CREATE TABLE IF  NOT EXISTS model_run_instance(
	id = BIGINT GENERATED BY DEFAULT AS IDENTITY(STARTS WITH 1) PRIMARY KEY
	batch_id BIGINT , 
	input_values BIGINT,
	output_values BIGINT


);

CREATE TABLE IF NOT EXISTS model_run_inputs(
	id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY,
	model_inputs VARChAR(4096) 


);

CREATE TABLE IF NOT EXISTS output_values (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY(STARTS WITH 1) PRIMARY KEY,
	model_outputs VARCHAR(4096)
);

CREATE TABLE IF NOT EXISTS batch_job(
	
	batch_id BIGINT GENERATED BY DEFAULT AS IDENTITY(STARTS WITH 1) PRIMARY KEY,
	model_id BIGINT,
	status VARCHAR(32)
	
);

